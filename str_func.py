def upper():
    """
    make letters upper
    just changes
    :return:
    """
    s = input("Ваша строка")
    print(f'{s.upper()}')
    return s

def first_letter_up():
    """
    make first letter upper
    just changes
    :return:
    """
    i = input("Ваша строка")
    capitalized_i = i.capitalize()
    print(f'{capitalized_i}')
    return print()

